package com.dunkware.xstream.model.metrics;

public class EntitySessionSignalMetrics {
	
	private int signalId; 
	private String signalIdent; 
	private int signalCount;
	
	public int getSignalId() {
		return signalId;
	}
	public void setSignalId(int signalId) {
		this.signalId = signalId;
	}
	public String getSignalIdent() {
		return signalIdent;
	}
	public void setSignalIdent(String signalIdent) {
		this.signalIdent = signalIdent;
	}
	public int getSignalCount() {
		return signalCount;
	}
	public void setSignalCount(int signalCount) {
		this.signalCount = signalCount;
	} 
	
	

}
