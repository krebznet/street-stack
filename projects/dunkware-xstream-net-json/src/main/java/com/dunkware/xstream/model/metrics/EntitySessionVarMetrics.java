package com.dunkware.xstream.model.metrics;

public class EntitySessionVarMetrics {
	
	private int varId; 
	private String varIdent; 
	private Number high; 
	private Number low;
	
	public int getVarId() {
		return varId;
	}
	public void setVarId(int varId) {
		this.varId = varId;
	}
	public String getVarIdent() {
		return varIdent;
	}
	public void setVarIdent(String varIdent) {
		this.varIdent = varIdent;
	}
	public Number getHigh() {
		return high;
	}
	public void setHigh(Number high) {
		this.high = high;
	}
	public Number getLow() {
		return low;
	}
	public void setLow(Number low) {
		this.low = low;
	} 
	
	

}
