package com.dunkware.trade.service.beach.web;

public class WebPlay {

	private String account; 
	private String name; 
	private String status; 
	private Number activeCapital; 
	private Number activeTrades; 
	private Number closedTrades; 
	private Number tradedCapital; 
	private Number realizedPL; 
	private Number unrealizedPL;
	
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Number getActiveCapital() {
		return activeCapital;
	}
	public void setActiveCapital(Number activeCapital) {
		this.activeCapital = activeCapital;
	}
	public Number getActiveTrades() {
		return activeTrades;
	}
	public void setActiveTrades(Number activeTrades) {
		this.activeTrades = activeTrades;
	}
	public Number getClosedTrades() {
		return closedTrades;
	}
	public void setClosedTrades(Number closedTrades) {
		this.closedTrades = closedTrades;
	}
	public Number getTradedCapital() {
		return tradedCapital;
	}
	public void setTradedCapital(Number tradedCapital) {
		this.tradedCapital = tradedCapital;
	}
	public Number getRealizedPL() {
		return realizedPL;
	}
	public void setRealizedPL(Number realizedPL) {
		this.realizedPL = realizedPL;
	}
	public Number getUnrealizedPL() {
		return unrealizedPL;
	}
	public void setUnrealizedPL(Number unrealizedPL) {
		this.unrealizedPL = unrealizedPL;
	}
	
	
		
 
}
