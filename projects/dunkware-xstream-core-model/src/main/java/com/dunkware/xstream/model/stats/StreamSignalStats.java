package com.dunkware.xstream.model.stats;

public class StreamSignalStats {
	
	private int sigId;
	private String sigIdent; 
	private int count;
	
	public int getSigId() {
		return sigId;
	}
	public void setSigId(int sigId) {
		this.sigId = sigId;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getSigIdent() {
		return sigIdent;
	}
	public void setSigIdent(String sigIdent) {
		this.sigIdent = sigIdent;
	}
	
	
	
	
	
	

}
